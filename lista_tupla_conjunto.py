#diferenca entre lista, tupla e conjunto
# lista - mutavel, pode ser alterada, ordenada, duplicada   
# tupla - imutavel, nao pode ser alterada, ordenada, duplicada
# conjunto - mutavel, nao pode ser ordenada, nao pode ter duplicatas
lista_produtos = ["iphone", "ipad", "airpod"]
tupla_produtos = ("iphone", "ipad", "airpod")       
conjunto_produtos = {"iphone", "ipad", "airpod"}
print("Lista de produtos:", lista_produtos)     
print("Tupla de produtos:", tupla_produtos)
print("Conjunto de produtos:", conjunto_produtos)
#quando usar cada um
# lista - quando precisa de uma coleção de itens que podem ser alterados, ordenados e duplicados    
# tupla - quando precisa de uma coleção de itens que não podem ser alterados, ordenados e duplicados
# conjunto - quando precisa de uma coleção de itens que podem ser alterados, mas não podem ser ordenados e não podem ter duplicatas
# exemplo de lista
lista_numeros = [1, 2, 3, 4, 5]
# exemplo de tupla
tupla_numeros = (1, 2, 3, 4, 5)
# exemplo de conjunto
conjunto_numeros = {1, 2, 3, 4, 5}
print("Lista de números:", lista_numeros)
print("Tupla de números:", tupla_numeros)
print("Conjunto de números:", conjunto_numeros)

