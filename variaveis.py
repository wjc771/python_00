# variaveis mutaveis e nao mutaveis 
# variaveis mutaveis sao aquelas que podem ser alteradas, como listas, dicionarios e conjuntos
# variaveis nao mutaveis sao aquelas que nao podem ser alteradas, como tuplas, strings e numeros    
# variaveis mutaveis
lista = [1, 2, 3, 4, 5] 
lista.append(6)  # Adiciona o número 6 à lista
print("Lista mutável:", lista)  # Exibe a lista após a modificação
# variaveis nao mutaveis
tupla = (1, 2, 3, 4, 5)  # Tupla é imutável
print("Tupla imutável:", tupla)  # Exibe a tupla, que não pode ser modificada
# variaveis nao mutaveis
string = "Hello, World!"  # String é imutável
print("String imutável:", string)  # Exibe a string, que não pode ser modificada
# variaveis nao mutaveis
numero = 42  # Número é imutável    
print("Número imutável:", numero)  # Exibe o número, que não pode ser modificado
# variaveis mutaveis
dicionario = {"chave1": "valor1", "chave2": "valor2"}  # Dicionário é mutável
dicionario["chave3"] = "valor3"  # Adiciona uma nova chave-valor ao dicionário  

print(dicionario)
